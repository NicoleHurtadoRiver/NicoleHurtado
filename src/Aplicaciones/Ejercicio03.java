    /**
     * Método contadoC hallar el arreglo en formato utiliza la variable contador
     * para captura del valor
     *
     * @param contador
     */
    public void contadoC() {
        // calcula el valor para cada elemento del arreglo
        for (int contador = 0; contador < arreglo.length; contador++) {
            arreglo[contador] = 2 + 2 * contador;;
            txtReporte.setText("reporte");
            // encabezados de columnas
            txtReporte.append("\n" + "Indice" + "\t Valor");
        }
        // imprime el valor de cada elemento del arreglo
        for (int contador = 0; contador < arreglo.length; contador++) {
            txtReporte.append("\n" + contador + "\t" + arreglo[contador]);
        } // fin de la clase InicArreglo
    }

